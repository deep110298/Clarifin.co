#!/usr/bin/env node
/**
 * Post-build script: reads the Vite-built index.html and emits a TypeScript
 * module that the Express server can import to serve the SPA shell on Vercel.
 *
 * Run AFTER `pnpm --filter @workspace/clarifin run build`.
 */
import { readFileSync, writeFileSync, mkdirSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");

const htmlPath = resolve(root, "artifacts/clarifin/dist/public/index.html");
const outPath = resolve(root, "artifacts/api-server/src/generated-index.ts");

const html = readFileSync(htmlPath, "utf8");

// Escape backticks and backslashes so the string is safe inside a template literal
const escaped = html.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");

const module = `// AUTO-GENERATED — do not edit by hand.
// Generated by scripts/generate-html-module.mjs after the Vite build.
export const indexHtml = \`${escaped}\`;
`;

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, module, "utf8");

console.log(`✅  Generated ${outPath}`);
